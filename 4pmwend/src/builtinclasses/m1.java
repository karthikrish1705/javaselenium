package builtinclasses;

public class m1 {
	
	public static void main(String[] args) {
		//Math => mathematical calculations
		// static in nature 
		
		System.out.println(Math.addExact(30, 40));
		System.out.println(Math.subtractExact(30, 4));
		System.out.println(Math.multiplyExact(30, 4));
		System.out.println(Math.floorDiv(20, 2));
		
		System.out.println(Math.sqrt(30));
		System.out.println(Math.cbrt(20));
		System.out.println(Math.pow(48, 3));
		
		System.out.println(Math.max(30, 1));
		System.out.println(Math.min(20, 10));
		
		System.out.println(Math.sin(30));
		System.out.println(Math.cos(20));
		System.out.println(Math.tan(10));
		System.out.println(Math.log(30));
		
		System.out.println(Math.round(30.98));
		
		
	}

}
