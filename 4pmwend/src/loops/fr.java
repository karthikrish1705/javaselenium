package loops;

public class fr {
	public static void main(String[] args) {
		
		
		/*
		 * for/while/do while
		 * 
		 * initialisation :   where to start 
		 * 
		 * condition checking : where to stop
		 *                forward :   <=end value
		 *                backward:   >=end value
		 *                
		 *inc/dec     :   diff betbween 2 consecutive numbers 
		 *
		 *
		 *for(initialisaiton; condition checking; inc/dec)
		 *{}
		 *
		 *4th table in reverse   40,36...0
		 *
		 *even numbers from 60 to 70   
		 *
		 **/
		
		for(int i=40; i>=0; i-=4)
		{
			System.out.println(i);
		}
	}

}
