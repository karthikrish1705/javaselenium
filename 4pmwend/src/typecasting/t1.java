package typecasting;

public class t1 {
	
	// typecasting: converting variable of one datatype to another datatype
	//    syntax:   (desired)  to be converted 
	
	public static void main(String[] args) {
		
		
		int age=30;
		System.out.println(age);
		
		// int => double 
		
		double r = (double)age;
		System.out.println(r);
		
		
		float av=3.98f;
		System.out.println(av);
		
		int r1 = (int)av;
		System.out.println(r1);
	}

}
